package it.dstech.controller;

public class GestioneOperazioni {
	
	public int somma(int num1, int num2) {		
		return num1+num2;
	}
	
	public int sottrazione(int num1, int num2) {		
		return num1-num2;
	}
	
	public double motiplicazione(int num1, int num2) {		
		return num1*num2;
	}
	
	public double divisione(int num1, int num2) {		
		return num1/num2;
	}

}
